x = 6
y = 2
z = 7

print(f"{x} + {y} = {x + y}") # Suma
print(f"{x} - {y} = {x - y}") # Resta
print(f"{x} * {y} = {x * y}") # Multiplicación
print(f"{x} / {y} = {x / y}") # División
print(f"{z} // {y} = {z // y}") # División al piso
print(f"{z} % {y} = {z % y}") # Módulo y se usa para saber si un número es par o impar, por ejemplo: 7 % 2 = 1 (impar) y 6 % 2 = 0 (par)
print(f"{x} ** {y} = {x ** y}") # Potencia

