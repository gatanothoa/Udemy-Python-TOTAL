# Integers (int): son numeros enteros, sin parte decimal.
# Se usan para contar, edades, cantidades, posiciones, etc.
edad = 28
cantidad_productos = 7
print("\nInteger:", edad, type(edad))
print("Otro integer:", cantidad_productos, type(cantidad_productos))


# Floats (float): son numeros con decimales.
# Se usan para precios, medidas, promedios o cualquier valor con precision decimal.
precio = 19.99
altura = 1.75
print("\nFloat:", precio, type(precio))
print("Otro float:", altura, type(altura))


# Ejemplo rapido de operaciones
print("\nSuma de integers:", 10 + 5)
print("Suma de floats:", 2.5 + 1.2)
print("Integer + float:", 3 + 2.5)